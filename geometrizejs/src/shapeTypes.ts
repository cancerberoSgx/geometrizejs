/**
 * Enumeration specifying the types of shapes that can be used. These can be combined to produce images
 * composed of multiple shape types. These are name and values: RECTANGLE: 0, ROTATED_RECTANGLE: 1, TRIANGLE:
 * 2, ELLIPSE: 3, ROTATED_ELLIPSE: 4, CIRCLE: 5, LINE: 6, QUADRATIC_BEZIER: 7 
 */
export declare class ShapeTypes {
  public static RECTANGLE: number
  public static ROTATED_RECTANGLE: number
  public static TRIANGLE: number
  public static ELLIPSE: number
  public static ROTATED_ELLIPSE: number
  public static CIRCLE: number
  public static LINE: number
  public static QUADRATIC_BEZIER: number
}
